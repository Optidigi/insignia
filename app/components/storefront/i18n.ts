export type TranslationStrings = typeof translations.en;

export function getTranslations(locale?: string): TranslationStrings {
  const lang = (locale || "en").split("-")[0] as Locale;
  return (translations as Record<string, TranslationStrings>)[lang] || translations.en;
}

export function detectLocale(): Locale {
  if (typeof navigator === "undefined") return "en";
  const lang = navigator.language.split("-")[0];
  return lang in translations ? (lang as Locale) : "en";
}

